#while loop
number = 20
while number <=25:
    print(number)
    number += 1

#Decreasing values
count = 105
while count >=100:
    print(count)
    count -=1

#for loop
for x in range(1,5):
    print(x)

for num in range(20,30,2):
    print(num)

#list
languages = ["python","kotlin","php"]
for lang in languages:
    print(lang)

